public class Identifiers {
	public static void main(String args[]) {
		/*
		 * Todas las variables Java deben identificarse con nombres únicos.
		 * 
		 * Estos nombres únicos se denominan identificadores.
		 * 
		 * Los identificadores pueden ser nombres cortos (como x e y) o nombres más
		 * descriptivos (edad, suma, totalVolume).
		 * 
		 * Nota: Se recomienda utilizar nombres descriptivos para Cree código
		 * comprensible y mantenible:
		 */
		// bien
		int minutesPerHour = 60;

		// Está bien, pero no es tan fácil entender lo que soy en realidad.
		int m = 60;

		/*
		 * Las reglas generales para nombrar variables son:
		 * 
		 * Los nombres pueden contener letras, dígitos, guiones bajos y signos de dólar
		 * Los nombres deben comenzar con una letra
		 * Los nombres deben comenzar con una letra minúscula y no puede contener
		 * espacios en blanco
		 * Los nombres también pueden comenzar con $ y _ (pero no lo usaremos en este
		 * tutorial)
		 * Los nombres distinguen entre mayúsculas y minúsculas ("myVar" y "myvar" son
		 * variables diferentes)
		 * Las palabras reservadas (como las palabras clave de Java, como o ) no se
		 * pueden utilizar como nombresintboolean
		 */
	}
}
