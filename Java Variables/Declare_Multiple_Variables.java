public class Declare_Multiple_Variables {
	public static void main(String args[]) {
		// Para declarar más de una variable del mismo tipo, puede Utilice una lista
		// separada por comas:
		int x = 5, y = 6, z = 50;
		System.out.println(x + y + z);
		// También puede asignar el mismo valor a varias variables en una línea:
		int x1, y1, z1;
		x1 = y1 = z1 = 50;
		System.out.println(x1 + y1 + z1);
	}
}
