public class Numbers {
	public static void main(String args[]) {
		/*
		 * Los tipos de números primitivos se dividen en dos grupos:
		 * 
		 * Los tipos enteros almacenan números enteros, positivos o negativos (como 123
		 * o -456), sin decimales. Los tipos válidos son , , y . El tipo que debe usar
		 * depende del valor numérico.byteshortintlong
		 * 
		 * Los tipos de coma flotante representan números con una parte fraccionaria,
		 * que contiene uno o más decimales. Hay dos tipos: y .floatdouble
		 */
		byte myNum = 100;
		System.out.println(myNum);
		short myNum2 = 5000;
		System.out.println(myNum2);
		int myNum3 = 100000;
		System.out.println(myNum3);
		// Tipos de coma flotante
		float myNum4 = 5.75f;
		System.out.println(myNum4);
		double myNum5 = 19.99d;
		System.out.println(myNum5);
	}
}
